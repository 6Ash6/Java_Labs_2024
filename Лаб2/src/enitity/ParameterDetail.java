package entity;

public class ParameterDetail {
    private Parameter name;
    private String value;

    // Getters and Setters

    public Parameter getName() {
        return name;
    }

    public void setName(Parameter name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
